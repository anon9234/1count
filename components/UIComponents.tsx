interface ButtonProps {
  children?: React.ReactNode;
  onClick?: () => void;
  variant?: 'primary' | 'secondary' | 'danger' | 'outline' | 'ghost';
  className?: string;
  disabled?: boolean;
  icon?: React.ReactNode;
  type?: 'button' | 'submit' | 'reset';
}

export const Button: React.FC<ButtonProps> = ({
  children,
  onClick,
  variant = 'primary',
  className = '',
  disabled = false,
  icon,
  type = 'button'
}) => {
  const base = "inline-flex items-center justify-center gap-2 px-4 py-2 rounded-xl font-medium transition-all focus:outline-none focus:ring-2 focus:ring-offset-1 disabled:opacity-50 disabled:cursor-not-allowed";
  
  const variants = {
    primary: "bg-accent text-white hover:bg-emerald-600 focus:ring-emerald-500 shadow-lg shadow-emerald-500/20",
    secondary: "bg-surface text-slate-200 border border-slate-700 hover:bg-slate-800 focus:ring-slate-700 shadow-sm",
    danger: "bg-red-950 text-red-400 border border-red-900 hover:bg-red-900 focus:ring-red-500",
    outline: "border-2 border-slate-700 text-slate-400 hover:border-slate-600 hover:text-slate-200",
    ghost: "text-slate-400 hover:text-slate-200 hover:bg-slate-800/50"
  };

  return (
    <button
      type={type}
      onClick={onClick}
      disabled={disabled}
      className={`${base} ${variants[variant]} ${className}`}
    >
      {icon}
      {children}
    </button>
  );
};

